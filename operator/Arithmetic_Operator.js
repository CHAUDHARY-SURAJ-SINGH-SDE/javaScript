/*Assignment operator are used to perform Assignment operation  */
//Addition '+' add left operand to right operand
//Substraction '-' subtract left operand from right operand
//multiplication '*' multiply left operand with right operand
//division '/' divide left operand by right operand and return quatant
//Modulo '%' divide left operand by right operand and return 'reminder'
//Exponents '**' calulate the power of left opwerands by right operands time
//increment '++' increment the value of operand by 1
//decrement '--' decrement the value of operand by 1
var num1 = 10;
var num2 = 15;
console.log(num1 + num2);//25
console.log(num1 - num2);//-5
console.log(num1 * num2);//150
console.log(num2 / num1);//1.5
console.log(num2 % num1);//5
console.log(num1 ** num2);//1000000000000000
console.log(++num1);//11 pre incrment
console.log(--num1);//10 pe decrement
console.log(num2++);//15 post increment
console.log(num2--);//15 post decrement